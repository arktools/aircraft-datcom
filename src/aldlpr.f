      SUBROUTINE ALDLPR(ALPHA,DELTA,VAR,NALPHA,NDELTA)
C
      DIMENSION VAR(200),ALPHA(20),DELTA(10)
      DIMENSION VFOR(23),VFS(3)
      EQUIVALENCE (VFOR(1),VFS(1))
      CHARACTER*4 VAR,VFOR,BLANK,VFS,E9P2,C1XC,RP,RP1,RP2,RP3
      DATA BLANK/'    '/
      DATA VFS /'(3X,','F5.1',',7X,'/
      DATA E9P2/'E9.2'/
      DATA C1XC/',1X,'/
      DATA RP1/'6X,A'/
      DATA RP2/'4,  '/
      DATA RP /')   '/
      DATA RP3/'4)  '/
 1000 FORMAT(1H0,7X,8HDELTA =  ,F5.1,5X,F5.1,5X,F5.1,5X,F5.1,5X,F5.1,5X,
     1F5.1,5X,F5.1,5X,F5.1,5X,F5.1,5X,F5.1)
 1010 FORMAT(3X,5HALPHA/1H0)
C     ----BUILD FORMAT
      K=4
      DO 1020 I=1,10
      VFOR(K)=E9P2
      K=K+1
      VFOR(K)=C1XC
 1020 K=K+1
      K=K-1
      VFOR(K)=RP
      WRITE(6,1000)(DELTA(I),I=1,NDELTA)
      WRITE(6,1010)
      JA=1
      DO 1050 I=1,NALPHA
      JN=JA+NDELTA-1
      IF(I.GT.1)GO TO 1040
C     ----LOGIC TO PRINT BLANKS *NOTE* COMPLETE COLUMN OF BLANKS ASSUMED
      K=1
      DO 1030 J=JA,JN
      IF(VAR(J).NE.BLANK)GO TO 1030
      II=2*(K-1)+4
      VFOR(II)=RP1
      VFOR(II+1)=RP2
      IF(K.NE.10)GO TO 1030
      VFOR(II+1)=RP3
 1030 K=K+1
 1040 WRITE(6,VFOR) ALPHA(I),(VAR(J),J=JA,JN)
 1050 JA=JA+10
      RETURN
      END

      FUNCTION ARCSIN(A)
C
C     ----TAKES ARCSIN OF ARGUMENT USING STANDARD FORTRAN.
C
      COMMON/CONSNT/ PI
      IF(ABS(A).EQ.1.)GO TO 1020
      IF(ABS(A).GT.1.0)GO TO 1000
      ARCSIN=ATAN(A/SQRT(1.-A**2))
      RETURN
 1000 WRITE(6,1010)A
      ARCSIN=1000.
 1010 FORMAT(17H0ERROR-ARCSIN OF ,E12.5)
      GO TO 1030
 1020 ARCSIN=PI/2.*A/ABS(A)
      RETURN
 1030 CONTINUE
      RETURN
      END

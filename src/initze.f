      SUBROUTINE INITZE
C
C***  IDEAL OUTPUT MATRIX
C
C             BLOCK    PRINT     IOM
C             NAME     FLAG     ARRAY
C
      COMMON /IBODY/   PBODY,  BODY(400)
      COMMON /IWING/   PWING,  WING(400)
      COMMON /IHT/     PHT,    HT(380)
      COMMON /IVT/     PVT,    VT(380)
      COMMON /IVF/     PVF,    VF(380)
      COMMON /IBW/     PBW,    BW(380)
      COMMON /IBH/     PBH,    BH(380)
      COMMON /IBV/     PBV,    BV(380)
      COMMON /IBWH/    PBWH,   BWH(380)
      COMMON /IBWV/    PBWV,   BWV(380)
      COMMON /IBWHV/   PBWHV,  BWHV(380)
      COMMON /IPOWER/  PPOWER, POWER(200)
      COMMON /IDWASH/  PDWASH, DWASH(60)
C
      LOGICAL PBODY, PWING, PHT, PVT, PVF, PBW, PBH, PBV, PBWH, PBWV,
     1        PBWHV, PPOWER, PDWASH
C
C***  INPUT DATA BLOCKS
C
      COMMON /FLGTCD/ FLC(160)
      COMMON /OPTION/ XX(4)
      COMMON /SYNTSS/ XCG, XW, ZW, ALIW, ZCG, XH, ZH, ALIH, XV,
     1                VERTUP, HINAX, XVF, SCALE, ZV, ZVF, YV, YF,
     2                PHIV, PHIF
      COMMON /BODYI/  BODYIN(129)
      COMMON /WINGI/  WINGIN(101)
      COMMON /VTI/    VTIN(154), TVTIN(8), VFIN(154)
      COMMON /HTI/    HTIN(154)
      COMMON /POWER/  PWIN(29), LBIN(21)
      COMMON /FLAPIN/ F(138)
C
C***  COMPUTATIONAL BLOCKS
C
      COMMON /WINGD/  A(195), B(49)
      COMMON /SBETA/  STB(135), TRA(108), TRAH(108), STBH(135)
      COMMON /BDATA/  BD(762)
      COMMON /WHWB/   FACT(182), WB(39), HB(39)
      COMMON /WBHCAL/ WBT(156)
      COMMON /HTDATA/ AHT(195), BHT(49)
      COMMON /VTDATA/ AVT(195), AVF(195)
      COMMON /WHAERO/ C(51), D(55), CHT(51), DHT(55), DVT(55), DVF(55)
      COMMON /POWR/   PW(315)
      COMMON /SUPWBB/  SWB(61), SHB(61)
      COMMON /SUPDW/  DWA(237)
      COMMON /SUPWH/  GR(303)
      COMMON /SUPBOD/ SBD(229)
      COMMON /LEVEL2/ SECOND(23)
C
      COMMON /CONSNT/CONST(4)
      EQUIVALENCE (CONST(3),UNUSED),(DONE,SECOND(15)),(DOL2,SECOND(16))
      LOGICAL BLF,ROUNDN,VERTUP,DONE,DOL2
      REAL LBIN
      DIMENSION X(19)
      EQUIVALENCE (BLF,LBIN(14)),(ROUNDN,LBIN(17)),(X(1),XCG)
C
C     ----FIX WITH ENTRIES TO INITIALIZE DIFFERENT PARTS
C
      DO 1000 I=1,195
 1000 A(I)=UNUSED
      DO 1010 I=1,49
 1010 B(I) = UNUSED
      DO 1020 I=1,51
 1020 C(I)=UNUSED
      DO 1030 I=1,55
 1030 D(I)=UNUSED
      DO 1040 I=1,762
 1040 BD(I)=UNUSED
      DO 1050 I=1,39
      HB(I)=UNUSED
 1050 WB(I)=UNUSED
      DO 1060 I=1,4
 1060 XX(I)=UNUSED
      DO 1070 I=1,19
      IF(I .NE. 10)X(I)=UNUSED
 1070 CONTINUE
      VERTUP=.TRUE.
      DO 1080 I=1,160
 1080 FLC(I)=UNUSED
      DO 1090 I=1,182
 1090 FACT(I)=UNUSED
      DO 1100 I=1,156
 1100 WBT(I)=UNUSED
      DO 1110 I=1,49
 1110 BHT(I)=UNUSED
      DO 1120 I=1,195
      AVF(I)=UNUSED
 1120 AVT(I)=UNUSED
      DO 1130 I=1,195
 1130 AHT(I)=UNUSED
      DO 1140 I=1,315
 1140 PW(I)=UNUSED
      DO 1170 I=1,51
 1170 CHT(I)=UNUSED
      DO 1180 I=1,55
 1180 DHT(I)=UNUSED
      DO 1190 I=1,55
      DVF(I)=UNUSED
 1190 DVT(I)=UNUSED
      DO 1200 I=1,135
      STBH(I)=UNUSED
 1200 STB(I)=UNUSED
      DO 1210 I=1,229
 1210 SBD(I)=UNUSED
      DO 1215 I=1,23
 1215 SECOND(I)=UNUSED
      DO 1220 I=1,303
 1220 GR(I)=UNUSED
      DO 1230 I=1,61
      SHB(I)=UNUSED
1230  SWB(I)=UNUSED
      DO 1240 I=1,29
 1240 PWIN(I)=UNUSED
      DO 1250 I=1,237
 1250 DWA(I)=UNUSED
      DO 1260 I=1,129
 1260 BODYIN(I)=UNUSED
      DO 1270 I=1,101
 1270 WINGIN(I)=UNUSED
      WINGIN(7) = 0.
      WINGIN(8) = 0.
      DO 1280 I=1,108
      TRAH(I)=UNUSED
 1280 TRA(I)=UNUSED
      DO 1290 I=1,138
 1290 F(I)=UNUSED
      DO 1300 I=1,154
      HTIN(I) = UNUSED
      VTIN(I) = UNUSED
      VFIN(I) = UNUSED
      IF(I .GT. 21) GO TO 1300
      LBIN(I) = UNUSED
      IF(I .GT. 8) GO TO 1300
      TVTIN(I) = UNUSED
 1300 CONTINUE
      VTIN(7) = 0.
      VTIN(8) = 0.
      VFIN(7) = 0.
      VFIN(8) = 0.
      HTIN(7) = 0.
      HTIN(8) = 0.
      BLF=.FALSE.
      ROUNDN=.FALSE.
      CALL ZERANG(A(106))
      CALL ZERANG(A(112))
      CALL ZERANG(A(138))
      CALL ZERANG(AHT(106))
      CALL ZERANG(AVT(106))
      CALL ZERANG(AVF(106))
      CALL ZERANG(AHT(112))
      CALL ZERANG(AVT(112))
      CALL ZERANG(AVF(112))
      CALL ZERANG(AHT(138))
      CALL ZERANG(AVT(138))
      CALL ZERANG(AVF(138))
      CALL ZERANG(A(153))
      CALL ZERANG(A(147))
      CALL ZERANG(AHT(153))
      CALL ZERANG(AHT(147))
      CALL ZERANG(AVT(153))
      CALL ZERANG(AVT(147))
      CALL ZERANG(AVF(153))
      CALL ZERANG(AVF(147))
      DO 1310 I=1,400
         BODY(I)=UNUSED
 1310 WING(I)=UNUSED
      DO 1320 I=1,380
      HT(I)=UNUSED
      VT(I)=UNUSED
      VF(I)=UNUSED
      BW(I)=UNUSED
      BH(I)=UNUSED
      BV(I)=UNUSED
      BWH(I)=UNUSED
      BWV(I)=UNUSED
 1320 BWHV(I)=UNUSED
      DO 1330 I=1,200
 1330 POWER(I)=UNUSED
      DO 1340 I=1,60
 1340 DWASH(I)=UNUSED
      PBODY=.FALSE.
      PWING=.FALSE.
      PHT=.FALSE.
      PVT=.FALSE.
      PVF=.FALSE.
      PBW=.FALSE.
      PBH=.FALSE.
      PBV=.FALSE.
      PBWH=.FALSE.
      PBWV=.FALSE.
      PBWHV=.FALSE.
      PPOWER=.FALSE.
      PDWASH=.FALSE.
      DONE=.FALSE.
      DOL2=.FALSE.
      RETURN
      END

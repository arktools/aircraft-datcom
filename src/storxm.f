      SUBROUTINE STORXM
C
C***  STORE EXTRAPOLATION MESSAGE DATA
C
      INTEGER ROUT
      COMMON /IBODY/   PB, AFIG01(121), AFIG02(121)
      COMMON /IWING/   PW, AFIG03(121), AFIG04(121)
      COMMON /IHT/    PHT, AFIG05(121), AFIG06(121)
      COMMON /IVT/    PVT, AFIG07(121), AFIG08(121)
      COMMON /IVF/    PVF, AFIG09(121), AFIG10(121)
      COMMON /IBW/    PBW, AFIG11(121), AFIG12(121)
      COMMON /IBH/    PBH, AFIG13(121), AFIG14(121)
      COMMON /IBV/    PBV, JFIG01(121), JFIG02(121)
      COMMON /IBWH/  PBWH, JFIG03(121), JFIG04(121)
      COMMON /IBWV/  PBWV, JFIG05(121), JFIG06(121)
      COMMON /IBWHV/PBWHV, JFIG07(121), JFIG08(121)
      COMMON /WINGD/       JFIG09(121)
      COMMON /SBETA/       JFIG10(121), JFIG11(121), JFIG12(121)
      COMMON /BDATA/       JFIG13(121), JFIG14(121), JFIG15(121),
     1                     JFIG16(121), JFIG17(121)
      COMMON /WHWB/        JFIG18(121)
      COMMON /WBHCAL/      JFIG19(121)
      COMMON /HTDATA/      JFIG20(121)
      COMMON /VTDATA/ LFIGN(121),LFIGO(121),IOVLY,IOVL,NSTQ,NSTP,FINALR,
     1                 NFIG,IFIGN
      COMMON /WHAERO/ LFIGS(121), IFIGST(20), IEXCD(4), MSSCL(2),
     1                ROUT(2), XLL(4), XUL(4), IEXTRL(4,2),
     2                IEXTRU(4,2), XVAL(4)
      LFIGN(NFIG)=IFIGN
      AFIG01(NFIG)=NSTP
      AFIG02(NFIG)=XVAL(1)
      AFIG03(NFIG)=XVAL(2)
      AFIG04(NFIG)=XVAL(3)
      AFIG05(NFIG)=XVAL(4)
      AFIG06(NFIG)=XLL(1)
      AFIG07(NFIG)=XLL(2)
      AFIG08(NFIG)=XLL(3)
      AFIG09(NFIG)=XLL(4)
      AFIG10(NFIG)=XUL(1)
      AFIG11(NFIG)=XUL(2)
      AFIG12(NFIG)=XUL(3)
      AFIG13(NFIG)=XUL(4)
      AFIG14(NFIG)=FINALR
      JFIG01(NFIG)=MSSCL(1)
      JFIG02(NFIG)=MSSCL(2)
      JFIG03(NFIG)=ROUT(1)
      JFIG04(NFIG)=ROUT(2)
      JFIG05(NFIG)=IEXTRL(1,1)
      JFIG06(NFIG)=IEXTRL(2,1)
      JFIG07(NFIG)=IEXTRL(3,1)
      JFIG08(NFIG)=IEXTRL(4,1)
      JFIG09(NFIG)=IEXTRL(1,2)
      JFIG10(NFIG)=IEXTRL(2,2)
      JFIG11(NFIG)=IEXTRL(3,2)
      JFIG12(NFIG)=IEXTRL(4,2)
      JFIG13(NFIG)=IEXTRU(1,1)
      JFIG14(NFIG)=IEXTRU(2,1)
      JFIG15(NFIG)=IEXTRU(3,1)
      JFIG16(NFIG)=IEXTRU(4,1)
      JFIG17(NFIG)=IEXTRU(1,2)
      JFIG18(NFIG)=IEXTRU(2,2)
      JFIG19(NFIG)=IEXTRU(3,2)
      JFIG20(NFIG)=IEXTRU(4,2)
      RETURN
      END

         CA(J)=CXP(J)
         CM(J)=CMP(J)
         GO TO 1110
 1100    CONTINUE
         INCN=0
         INCA=0
         INCM=0
         CALL TBFUNX(FLC(J+22),CN(J),DYDX,NALPHA,ALPHAP(1),CNP(1),CCN,
     1               INCN,MI,NG,0,0,4H CN ,1,ROUTID)
         CALL TBFUNX(FLC(J+22),CA(J),DYDX,NALPHA,ALPHAP(1),CXP(1),CCA,
     1               INCA,MI,NG,0,0,4H CA ,1,ROUTID)
         CALL TBFUNX(FLC(J+22),CM(J),DYDX,NALPHA,ALPHAP(1),CMP(1),CCM,
     1               INCM,MI,NG,0,0,4H CM ,1,ROUTID)
 1110    CONTINUE
         SINA=SIN(FLC(J+22)/RAD)
         COSA=COS(FLC(J+22)/RAD)
         CL(J)=CN(J)*COSA-CA(J)*SINA
         CD(J)=CA(J)*COSA+CN(J)*SINA
         IF(CN(J).EQ.0.0) CN(J)=1.0E-20
         XCPOLR(J)=CM(J)/CN(J)
 1120 CONTINUE
C
C*************WING, WING-BODY LATERAL STABILITY CALCULATIONS************
C        WING-BODY SIDEFORCE DERIVATIVE NEAR ZERO NORMAL FORCE**
C
      ARG1=SBS/SREF
C
C********************** FIGURE 5.5.1.1-6 *******************************
C
      CALL TBFUNX(ARG1,KYBNO,DYDX,12,X5516,Y5516,W1,N1,MI,NG,
     1 0,2,Q55116,3,ROUTID)
C
C        WING-BODY YAWING MOMENT DERIVATIVE NEAR ZERO NORMAL FORCE**
C
      ARG1=SBSLB/SBS
C
C        FIGURE 5.5.3.1-6
C
      CALL TBFUNX(ARG1,XCPXC,DYDX,19,X5536,Y5536,W2,N2,MI,NG,
     1            0,0,Q55316,3,ROUTID)
      XCPP=XCPXC*XCENSB
      AKN1=-.006*(XCG-XCENW)/(RAD*BB)
      AKN2=(XCG-XCPP)/(RAD*BB)
C
C*****WING, WING-BODY ROLLING MOMENT DERIVATIVE NEAR ZERO NORMAL FORCE**
C        FIGURE 5.5.2.1-8A
C
      CALL TBFUNX(THETAD,DKLCNO,DYDX,18,X5528,Y5528,W3,N3,WI,NT,
     1            2,2,Q5218A,3,ROUTID)
      IF(ROUNDN)GO TO 1130
      DKLCNB=0.0
      GO TO 1140
 1130 ARG1=1.-(4.*TAN(THETAD/RAD)/AR)
C
C        FIGURE 5.5.2.1-8B
C
      CALL TLINEX(X1558B,X2558B,Y558B,  6, 12,THETAD,ARG1,DKLCNB,
     1            2,0,2,2,Q5218B,3,ROUTID)
 1140 KLBCNO=1./RAD*(DKLCNO+DKLCNB)
C
C**************PARAMETERS REQUIRED TO CALCULATE (KYB)J *****************
C
      ARG6=SBS/SREF
      IF(R3LEOB.NE.UNUSED)GO TO 1150
      ARG1=DELTEP
      CALL TLINEX(X1559,X2559,Y559,  7, 10,ARG1,ARG6,KYCN20,
     1            0,0,1,1,Q55129,3,ROUTID)
      GO TO 1160
 1150 ARG1=R3LEOB
      CALL TLINEX(X1558,X2558,Y558, 14, 11,ARG1,ARG6,KYCN20,
     1            0,0,0,2,Q55128,3,ROUTID)
 1160 CONTINUE

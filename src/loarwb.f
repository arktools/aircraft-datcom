C**************PARAMETERS REQUIRED TO CALCULATE (KLB)J *****************
C        FIGURE 5.5.2.2-13
C
      CALL TBFUNX(ARG6,DKCKCC,DYDX,12,X55213,Y55213,W8,N8,MI,NG,
     1            0,0,Q52213,3,ROUTID)
      IF(R3LEOB.NE.UNUSED)GO TO 1170
C
C********************** FIGURE 5.5.2.2-12B *****************************
C
      CALL TBFUNX(DELTEP,KCKCC2,DYDX, 8,X5522B,Y5522B,W6,N6,MI,NG,
     1            0,2,Q5522B,3,ROUTID)
      GO TO 1180
 1170 CONTINUE
C
C        FIGURE 5.5.2.2-12A
C
      CALL TBFUNX(R3LEOB,KCKCC2,DYDX,19,X5522A,Y5522A,W7,N7,MI,NG,
     1            0,2,Q5522A,3,ROUTID)
 1180 CONTINUE
      KCCA20=1./(1.+.152/TAN(THETAD/RAD))*DKLCNO
      BPART=KCKCC2+DKCKCC
C
C*****WING, WING-BODY DERIVATIVES VARIATION WITH ANGLE OF ATTACK********
C
      DO 1190 J=1,NALPHA
C
C************** SIDEFORCE DERIVATIVE ***********************************
C
         KYB(J)=(1./RAD)*(KYBNO+KYCN20*CNP(J)**2)
C
C************ YAWING MOMENT DERIVATIVE *********************************
C
         KNB(J)=(KYB(J)+.006)*AKN2+AKN1
C
C************ ROLLING MOMENT DERIVATIVE ********************************
C
         CPART=CNP(J)/RAD
         APART=DKLCNO*(1.-(ALPHAP(J)/20.)**2)
         KLB(J)=CPART*(APART+(BPART*KCCA20+DKLCNB)*(ALPHAP(J)/20.)**2)
 1190 CONTINUE
      INKY=0
      INKN=0
      INKL=0
      DO 1210 J=1,NALPHA
         IF(ALPHA0.NE.0.0) GO TO 1200
         KYBA(J)=KYB(J)
         KNBA(J)=KNB(J)
         KLBA(J)=KLB(J)
         GO TO 1210
 1200    CALL TBFUNX(FLC(J+22),KYBA(J),DYDX,NALPHA,ALPHAP(1),KYB(1),C1,
     1               INKY,MI,NG,0,0,4HKYB ,1,ROUTID)
         CALL TBFUNX(FLC(J+22),KNBA(J),DYDX,NALPHA,ALPHAP(1),KNB(1),C2,
     1               INKN,MI,NG,0,0,4HKNB ,1,ROUTID)
         CALL TBFUNX(FLC(J+22),KLBA(J),DYDX,NALPHA,ALPHAP(1),KLB(1),C3,
     1               INKL,MI,NG,0,0,4HKLB ,1,ROUTID)
 1210 CONTINUE
      RETURN

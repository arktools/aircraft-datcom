      SUBROUTINE XNAM15(IOP)
C
      COMMON /VTI/    AG(162)
      COMMON /CONSNT/ PI,DEG,UNUSED,RAD,KAND
C
      INTEGER TVTPAN
      LOGICAL EOF
C
      DIMENSION LENG(8),LDMG(8),TVTPAN(23),LOCG(8)
      DIMENSION NLNAME(6)
C
      DATA NLNAME / 4HT   ,4HV   ,4HT   ,4HP   ,4HA   ,4HN   /
      DATA LENG / 3,2,3,2,2,6,3,2 /
      DATA LDMG / 8*1 /
      DATA LOCG / 155,156,157,158,159,160,161,162 /
      DATA TVTPAN /   4HB   ,4HV   ,4HP   ,4HB   ,4HV   ,4HB   ,
     1  4HD   ,4HV   ,4HB   ,4HH   ,4HS   ,4HV   ,4HV   ,4HP   ,
     2  4HH   ,4HI   ,4HT   ,4HE   ,4HV   ,4HL   ,4HP   ,4HZ   ,
     3  4HP   /
C
C**   IF IOP EQUAL ZERO READ NAMELIST TVTPAN
C**   IF IOP EQUAL ONE WRITE NAMELIST TVTPAN
C
      IF(IOP .EQ. 0)
     1  CALL NAMER(KAND,9,NLNAME,6,TVTPAN,23,LENG,8,LDMG,AG,162,
     2             LOCG,EOF)
      IF(IOP .EQ. 1)
     1  CALL NAMEW(KAND,6,NLNAME,6,TVTPAN,23,LENG,8,LDMG,AG,162,LOCG)
C
      RETURN
      END

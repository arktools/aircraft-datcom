      FUNCTION ARCCOS(A)
C
C     -----USED TO FIND ARCCOS USING STANDARD FORTRAN ONLY.
C
      COMMON/CONSNT/ PI
      IF(A.EQ.0.0)GO TO 1030
      IF(ABS(A).LE.1.0)GO TO 1000
C
C     ----IF HERE USE THE INVERSE COSH IF A .GT. 1.0
C
      X=ALOG(ABS(A+SQRT(A**2-1.)))
      GO TO 1010
 1000 X=ATAN(SQRT(1.-A**2)/A)
      IF(X.LT.0.0)GO TO 1020
 1010 ARCCOS=X
      RETURN
 1020 X=PI+X
      GO TO 1010
 1030 X=PI/2.
      GO TO 1010
      END

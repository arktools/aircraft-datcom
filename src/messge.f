      SUBROUTINE MESSGE(ROUT,MESS,X1,X2,X3,X4,MSSCL)
C
C     ----THIS SUBROUTINE WRITES TO UNIT 12 WHEN EXTRAPOLATION IS
C         REQUIRED IN THE TABLE LOOKUP SUBROUTINES.
C
      COMMON /OVERLY/ NN(5),NF,NM(2),IOVLY
      DIMENSION ROUT(2),MESS(1),X1(1),X2(1),X3(1),X4(1),MSSCL(21),
     1          RL(21),ML(13)
      EQUIVALENCE (RL(1),ML(1))
      IF(NF .LT. 0) RETURN
      NSTQ=MSSCL(3)
      ML(1)=MSSCL(4)
      NSTP=MSSCL(5)
      ML(2)=MSSCL(6)
      II=MSSCL(7)
      RL(6)=X1(1)
      RL(10)=X1(II)
      IF(NSTP.EQ.1)GO TO 1000
      ML(3)=MSSCL(10)
      II=MSSCL(11)
      RL(7)=X2(1)
      RL(11)=X2(II)
      IF(NSTP.EQ.2)GO TO 1000
      ML(4)=MSSCL(14)
      II=MSSCL(15)
      RL(8)=X3(1)
      RL(12)=X3(II)
      IF(NSTP.EQ.3)GO TO 1000
      ML(5)=MSSCL(18)
      II=MSSCL(19)
      RL(9)=X4(1)
      RL(13)=X4(II)
 1000 WRITE(12,1020) IOVLY,NSTQ,NSTP
      WRITE(12,1030)MSSCL(1),MSSCL(2),ROUT(1),ROUT(2),(MESS(L),L=1,NSTQ)
      DO 1010 L=1,NSTP
         WRITE(12,1040)RL(L+1),RL(L+5),RL(L+9),MSSCL(4*L+4),
     1        MSSCL(4*L+5)
 1010 CONTINUE
      WRITE(12,1050) RL(1)
 1020 FORMAT(3I3)
 1030 FORMAT(24A4)
 1040 FORMAT(3E12.5,2I2)
 1050 FORMAT(E12.5)
      RETURN
      END

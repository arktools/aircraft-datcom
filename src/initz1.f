      SUBROUTINE INITZ1
C
C***  INITIALIZE ARRAYS FOR PROGRAM USE
C
C     IDEAL OUTPUT MATRIX
C             BLOCK    PRINT     IOM
C             NAME     FLAG     ARRAY
C
      COMMON /IBODY/   PBODY,  BODY(400)
      COMMON /IWING/   PWING,  WING(400)
      COMMON /IHT/     PHT,    HT(380)
      COMMON /IVT/     PVT,    VT(380)
      COMMON /IBW/     PBW,    BW(380)
      COMMON /IBH/     PBH,    BH(380)
      COMMON /IBV/     PBV,    BV(380)
      COMMON /IVF/     PVF,    VF(380)
      COMMON /IBWH/    PBWH,   BWH(380)
      COMMON /IBWV/    PBWV,   BWV(380)
      COMMON /IBWHV/   PBWHV,  BWHV(380)
      COMMON /IPOWER/  PPOWER, POWER(200)
      COMMON /IDWASH/  PDWASH, DWASH(60)
C
      LOGICAL PBODY, PWING, PHT, PVT, PVF, PBW, PBH, PBV, PBWH, PBWV,
     1        PBWHV, PPOWER, PDWASH
C
C***  COMPUTATIONAL BLOCKS
C
      COMMON /SBETA/  STB(135), TRA(108), TRAH(108), STBH(135)
      COMMON /WHWB/   FACT(182), WB(39), HB(39)
      COMMON /WBHCAL/ WBT(156)
      COMMON /WHAERO/ C(51), D(55), CHT(51), DHT(55), DVT(55), DVF(55)
      COMMON /POWR/   PW(315)
      COMMON /SUPWBB/  SWB(61), SHB(61)
      COMMON /SUPDW/  DWA(237)
      COMMON /SUPWH/  GR(303)
      COMMON /SUPBOD/ SBD(229)
C
      COMMON /CONSNT/CONST(4)
      EQUIVALENCE (CONST(3),UNUSED)
C
      DO 1000 I=1,51
 1000 C(I)=UNUSED
      DO 1010 I=1,55
 1010 D(I)=UNUSED
      DO 1020 I=1,39
         HB(I)=UNUSED
 1020 WB(I)=UNUSED
      DO 1030 I=1,182
 1030 FACT(I)=UNUSED
      DO 1040 I=1,156
 1040 WBT(I)=UNUSED
      DO 1050 I=1,315
 1050 PW(I)=UNUSED
      DO 1060 I=1,51
 1060 CHT(I)=UNUSED
      DO 1070 I=1,55
 1070 DHT(I)=UNUSED
      DO 1080 I=1,55
         DVF(I)=UNUSED
 1080 DVT(I)=UNUSED
      DO 1090 I=1,135
         STBH(I)=UNUSED
 1090 STB(I)=UNUSED
      DO 1100 I=1,229
 1100 SBD(I)=UNUSED
      DO 1110 I=1,303
 1110 GR(I)=UNUSED
      DO 1120 I=1,61
         SHB(I)=UNUSED
 1120 SWB(I)=UNUSED
      DO 1130 I=1,237
 1130 DWA(I)=UNUSED
      DO 1140 I=1,108
         TRAH(I)=UNUSED
 1140 TRA(I)=UNUSED
      DO 1150 I=1,400
         BODY(I)=UNUSED
 1150 WING(I)=UNUSED
      DO 1160 I=1,380
         HT(I)=UNUSED
         VT(I)=UNUSED
         VF(I)=UNUSED
         BW(I)=UNUSED
         BH(I)=UNUSED
         BV(I)=UNUSED
         BWH(I)=UNUSED
         BWV(I)=UNUSED
 1160 BWHV(I)=UNUSED
      DO 1170 I=1,200
 1170 POWER(I)=UNUSED
      DO 1180 I=1,60
 1180 DWASH(I)=UNUSED
      RETURN
      END

      SUBROUTINE OUTPT4
C
C***  PRINTS HYPERSONIC CONTROL EFFECTIVENESS OUTPUTS
C
      COMMON /OVERLY/ NLOG,NMACH,M,NALPHA,IG
      COMMON /FLGTCD/ FLC(93)
      COMMON /FLAPIN/ F(16)
      COMMON /IBODY/  PB, BODY(200)
      COMMON /IWING/  PW, WING(200)
      COMMON /IHT/    PHT, HT(200)
      COMMON /IVT/    PVT, VT(200)
      COMMON /OPTION/ SREF,CBARR,ROUGFC,BLREF
      DIMENSION TEMP(200)
      DIMENSION ALPHA(20),DELTA(20)
      DIMENSION HYP(800)
      EQUIVALENCE (F(5),DELTA(1)),(ALPHA(1),FLC(23))
      DATA BLANK/4H    /
      DO 1000 I=1,200
         HYP(I    ) = BODY(I)
         HYP(I+200) = WING(I)
         HYP(I+400) = HT(I)
         HYP(I+600) = VT(I)
 1000 CONTINUE
      WRITE(6,1060)
      WRITE(6,1070)
      CALL PRCSID
      WRITE(6,1080)FLC(M+2),FLC(M+42),SREF,CBARR
      WRITE(6,1090)
      NDELTA=F(16)+.5
      CALL ALDLPR(ALPHA,DELTA,HYP(  1),NALPHA,NDELTA)
      WRITE(6,1100)
      CALL ALDLPR(ALPHA,DELTA,HYP(201),NALPHA,NDELTA)
      WRITE(6,1060)
      WRITE(6,1070)
      CALL PRCSID
      WRITE(6,1080)FLC(M+2),FLC(M+42),SREF,CBARR
      WRITE(6,1110)
      CALL ALDLPR(ALPHA,DELTA,HYP(401),NALPHA,NDELTA)
      WRITE(6,1120)
      CALL ALDLPR(ALPHA,DELTA,HYP(601),NALPHA,NDELTA)
      DO 1020 I=1,NALPHA
         DO 1020 J=1,NDELTA
            II=(I-1)*10+J
C            IF(HYP(II).EQ.0.0)GO TO 1010
            TEMP(II)=HYP(II+400)/HYP(II)
            GO TO 1020
C
C     ----PUT IN BLANKS FOR INDEFINITES.
C
C 1010       TEMP(II)=BLANK
 1020 CONTINUE
      WRITE(6,1060)
      WRITE(6,1070)
      CALL PRCSID
      WRITE(6,1080)FLC(M+2),FLC(M+42),SREF,CBARR
      WRITE(6,1130)
      CALL ALDLPR(ALPHA,DELTA,TEMP,NALPHA,NDELTA)
      DO 1050 I=1,NALPHA
         DO 1040 J=1,NDELTA
            II=(I-1)*10+J
C            IF(HYP(II+200).EQ.0.0)GO TO 1030
            TEMP(II)=-HYP(II+600)/HYP(II+200)
            GO TO 1040
C 1030       TEMP(II)=BLANK
 1040    CONTINUE
 1050 CONTINUE
      WRITE(6,1140)
      CALL ALDLPR(ALPHA,DELTA,TEMP    ,NALPHA,NDELTA)
 1060 FORMAT(1H1,26X,75HAUTOMATED STABILITY AND CONTROL METHODS PER APRI
     1L 1976 VERSION OF DATCOM     )
 1070 FORMAT(1H ,35X,
     154HAERODYNAMIC CONTROL EFFECTIVENESS AT HYPERSONIC SPEEDS)
 1080 FORMAT(20X,12HMACH NUMBER=,F5.2,57X,16HREYNOLDS NO./FT=,E9.2/1H0,
     1 20X,16HREF. AREA (FT2)=,F8.3,3X,28HMOMENT REFERENCE CENTER = HL,
     2 3X,22HLONG. REF. LENGTH(FT)=,F7.3)
 1090 FORMAT(1H0,25X,74H----------INCREMENT IN NORMAL FORCE PER SPANWISE
     1 FOOT OF CONTROL----------)
 1100 FORMAT(1H0,25X,47H----------INCREMENT IN AXIAL FORCE PER SPANWISE,
     126H FOOT OF CONTROL----------)
 1110 FORMAT(1H0,9X,107H----------INCREMENT IN PITCHING MOMENT DUE TO NO
     1RMAL FORCE INCREMENT PER SPANWISE FOOT OF CONTROL----------)
 1120 FORMAT(1H0,9X,106H----------INCREMENT IN PITCHING MOMENT DUE TO AX
     1IAL FORCE INCREMENT PER SPANWISE FOOT OF CONTROL----------)
 1130 FORMAT(1H0,31X,64H---------- CENTER-OF-PRESSURE LOCATION OF NORMAL
     1 FORCE----------)
 1140 FORMAT(1H0,31X,62H----------CENTER-OF-PRESSURE LOCATION OF AXIAL F
     1ORCE----------)
      RETURN
      END

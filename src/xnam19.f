      SUBROUTINE XNAM19(IOP)
C
      COMMON /FLAPIN/ AL(137)
      COMMON /CONSNT/ PI,DEG,UNUSED,RAD,KAND
C
      INTEGER TRNJET
      LOGICAL EOF
C
      DIMENSION LENL(12),LDML(12),TRNJET(38),LOCL(12)
      DIMENSION NLNAME(6)
C
      DATA NLNAME / 4HT   ,4HR   ,4HN   ,4HJ   ,4HE   ,4HT   /
      DATA LENL / 4,2,5,6,2,2,3,4,3,2,2,3 /
      DATA LDML / 3*10,-10,8*1 /
      DATA LOCL / 1,12,22,39,11,32,33,34,35,36,37,38 /
      DATA TRNJET /   4HT   ,4HI   ,4HM   ,4HE   ,4HF   ,4HC   ,
     1  4HA   ,4HL   ,4HP   ,4HH   ,4HA   ,4HL   ,4HA   ,4HM   ,
     2  4HN   ,4HR   ,4HJ   ,4HN   ,4HT   ,4HM   ,4HE   ,4HI   ,
     3  4HS   ,4HP   ,4HS   ,4HP   ,4HA   ,4HN   ,4HP   ,4HH   ,
     4  4HE   ,4HG   ,4HP   ,4HC   ,4HC   ,4HL   ,4HF   ,4HP   /
C
C**   IF IOP EQUAL ZERO READ NAMELIST TRNJET
C**   IF IOP EQUAL ONE WRITE NAMELIST TRNJET
C
      IF(IOP .EQ. 0)
     1 CALL NAMER(KAND,9,NLNAME,6,TRNJET,38,LENL,12,LDML,AL,137,
     2            LOCL,EOF)
      IF(IOP .EQ. 1)
     1  CALL NAMEW(KAND,6,NLNAME,6,TRNJET,38,LENL,12,LDML,AL,137,LOCL)
C
      RETURN
      END

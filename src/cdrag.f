C
C     COMPUTE RVI, D(7)
C
      D(7)= (D(9)/ABS(A(62))) * SQRT(1.-(FLC(I+2)*A(61))**2)
      TEMPI=0.0
C
C*********     IF RVI .GT. 1.3*10**5, USE FIGURE 4.1.5.2-53-B
C
      IF(D(7).GT.1.30E+05) GO TO 1060
      CALL FIG53A(D(7),TEMPI,D(5))
      GO TO 1070
 1060 CALL TBFUNX(TEMPI,D(5),DYDX,8,X53B,Y53B,C53B,I53B,MI,NG
     1            ,0,0,Q5253B,3,ROUTID)
C
C*********          OUTBOARD COMPUTATIONS                        *******
C     COMPUTE RVO, D(6)
C
 1070 D(6)= (D(8)/ABS(A(86))) * SQRT(1.-(FLC(I+2)*A(85))**2)
      TEMPO=A(6)*A(28)/A(85)
C
C*********     IF RVO .T. 1.3*10**5, USE FIGURE 4.1.5.2-53-B
C
      IF(D(6).GT.1.30E+05) GO TO 1080
      CALL FIG53A(D(6),TEMPO,D(4))
      GO TO 1090
 1080 CALL TBFUNX(TEMPO,D(4),DYDX,8,X53B,Y53B,C53B,I53B,MI,NG
     1            ,0,0,Q5253B,3,ROUTID)
C
C*********     COMPUTE R-PRIME,D(1)
C
 1090 D(1)=D(5)*A(23)/AIN(3)+D(4)*(1.-(A(23)/AIN(3)))
      TEMP=(AINN(101)*SREF/A(3))*RAD/A(7)
C
C*********     COMPUTE E, D(30)
C
      D(30)=TEMP/(D(1)*TEMP+(1.-D(1))*PI)
C
C*********               CRANKED WING CONFIGURATION
C*********     FIGURE 4.1.5.2-54 ( TO GET DELTA(CDL)J,  D(25) )
C
      DO 1100 M=1,NALPHA
         TEMP=(ABS(AINN(M+20))/A(7))*SREF/A(3)
         CALL TLINEX(X154,X254,Y54,12,14,TEMP,A(7),D(25)
     1              ,0,0,0,0,Q15254,3,ROUTID)
C
C*********     COMPUTE (CDL)J AND CDJ
C
         D(34)=(AINN(M+20)**2/(PI*D(30)*A(7)))*SREF/A(3)+D(25)*A(3)/SREF
         D(M+35)= D(34)
         D(35)=D(20)+D(34)
         AINN(M)=D(35)
 1100 CONTINUE
      GO TO 1130
C
C*********     DOUBLE-DELTA OR CURVED WING CONFIGURATION         *******
C*********     COMPUTE (CDL) J AND CDJ
C
 1110 CONTINUE
      DO 1120 M=1,NALPHA
         D(34)=0.95*AINN(M+20)*TAN(B(M+22)/RAD)*D(3)
         D(M+35)= D(34)
         D(35)=D(20)+D(34)
         AINN(M)=D(35)
 1120 CONTINUE
 1130 CONTINUE
      B(46)=D(20)
      IF(A(38) .EQ. 0.00001) A(38)=0.0
      IF(A(62) .EQ. 0.00001) A(62)=0.0
      IF(A(86) .EQ. 0.00001) A(86)=0.0
      IF(.NOT.BO)RETURN
      AINN(101)=SAVE1
      DO 1140 KK=1,NALPHA
         AINN(KK+20)=SAVE2(KK)
 1140 CONTINUE
      RETURN
      END

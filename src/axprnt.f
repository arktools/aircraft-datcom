      SUBROUTINE AXPRNT(A,AIN,CDFI,CDFO,CDO,CDF,XW,NN,NM,XL,SC,XBN)
C
C     PRINT AUXILIARY PANEL OUTPUTS
C
      COMMON /CONSNT/ PI,DEG,UNUSED,RAD
      DIMENSION A(195),AIN(154),WTYPE(4),FOR(38),DATA(9),
     1          P(1),Q(1),R(1),S(1),T(1),ICAR(9)
      LOGICAL NN,NM
	CHARACTER*4 FOR
      DATA WTYPE / 4HSTRA, 4HDOUB, 4HCRAN, 4HCURV /
      DATA FOR /
     1 4H(1H+, 4H,19X, 4H,1X,, 4HE11., 4H4,1X, 4H,1X , 4H,1X,, 4HF6. ,
     2 4H3,1X, 4H,1X , 4H,1X,, 4HE11., 4H4,1X, 4H,1X , 4H,1X,, 4HF7. ,
     3 4H3,1X, 4H,1X , 4H,1X,, 4HE10., 4H3,1X, 4H,1X , 4H,1X,, 4HE10.,
     4 4H3,1X, 4H,1X , 4H,1X,, 4HE10., 4H3,1X, 4H,1X , 4H,1X,, 4HE10.,
     5 4H3,1X, 4H,1X , 4H,1X,, 4HE10., 4H3   , 4H)   /
      DATA ICAR / 13,8,13,9,5*12 /
C
C     INBOARD THEORITICAL
C
      SOT=0.
      IF(AIN(15) .EQ. WTYPE(1)) GO TO 1000
      DATA(1)=(AIN(6)+AIN(5))*(AIN(4)-AIN(2))
      SOT=DATA(1)
      DATA(1)=DATA(1)*XL*XL/(SC*SC)
      DATA(2)=AIN(5)/AIN(6)
      DATA(3)=(2.*(AIN(4)-AIN(2)))**2/DATA(1)
      DATA(4)=A(64)
      DATA(5)=2.*AIN(6)*(1.+DATA(2)*(1.+DATA(2)))/(3.*(1.+DATA(2)))
      DATA(7)=(AIN(4)-AIN(2))*(1.+2.*DATA(2))/(3.*(1.+DATA(2)))
      DATA(6)=A(62)*DATA(7)+XW+0.25*DATA(5)+XBN
      DATA(5)=DATA(5)*XL/SC
      DATA(6)=DATA(6)*XL/SC
      DATA(7)=DATA(7)*XL/SC
      DATA(8)=-UNUSED
      DATA(9)=-UNUSED
      WRITE(6,1010)
      CALL SWRITE(9,FOR,38,ICAR,1,DATA(1),DATA(2),DATA(3),DATA(4),
     1 DATA(5),DATA(6),DATA(7),DATA(8),DATA(9),P,Q,R,S,T,NN,NM)
C
C     INBOARD EXPOSED
C
      DATA(1)=A(1)
      DATA(1)=DATA(1)*XL*XL/(SC*SC)
      DATA(2)=A(26)
      DATA(3)=A(5)
      DATA(4)=A(64)
      DATA(5)=A(15)
      DATA(7)=A(32)+AIN(4)-AIN(3)
      DATA(6)=A(62)*DATA(7)+XW+0.25*DATA(5)+XBN
      DATA(5)=DATA(5)*XL/SC
      DATA(6)=DATA(6)*XL/SC
      DATA(7)=DATA(7)*XL/SC
      DATA(8)=-UNUSED
      DATA(9)=CDFI
      IF(CDFI .EQ. UNUSED) DATA(9)=2.*UNUSED
      WRITE(6,1020)
      CALL SWRITE(9,FOR,38,ICAR,1,DATA(1),DATA(2),DATA(3),DATA(4),
     1 DATA(5),DATA(6),DATA(7),DATA(8),DATA(9),P,Q,R,S,T,NN,NM)
C
C     OUTBOARD PANEL
C
      DATA(1)=A(2)
      DATA(1)=DATA(1)*XL*XL/(SC*SC)
      DATA(2)=A(28)
      DATA(3)=A(6)
      DATA(4)=A(88)
      DATA(5)=A(17)
      DATA(7)=A(33)
      DATA(6)=A(86)*DATA(7)+XW+0.25*DATA(5)+XBN
      DATA(5)=DATA(5)*XL/SC
      DATA(6)=DATA(6)*XL/SC
      DATA(7)=DATA(7)*XL/SC
      DATA(8)=-UNUSED
      DATA(9)=CDFO
      IF(CDFO .EQ. UNUSED) DATA(9)=2.*UNUSED
      WRITE(6,1030)
      CALL SWRITE(9,FOR,38,ICAR,1,DATA(1),DATA(2),DATA(3),DATA(4),
     1 DATA(5),DATA(6),DATA(7),DATA(8),DATA(9),P,Q,R,S,T,NN,NM)
 1000 CONTINUE
C
C     TOTAL THEORITICAL
C
      DATA(1)=A(4)
      DATA(1)=DATA(1)*XL*XL/(SC*SC)
      DATA(2)=A(118)
      DATA(3)=A(120)
      DATA(4)=ARCCOS((SOT*COS(A(64)/RAD)+A(2)*COS(A(88)/RAD))/A(4))*RAD
      IF(AIN(15) .EQ. WTYPE(1)) DATA(4)=A(40)
      DATA(5)=A(122)
      DATA(6)=A(161)+XW+XBN
      DATA(7)=A(136)
      DATA(5)=DATA(5)*XL/SC
      DATA(6)=DATA(6)*XL/SC
      DATA(7)=DATA(7)*XL/SC
      DATA(8)=-UNUSED
      DATA(9)=-UNUSED
      WRITE(6,1040)
      CALL SWRITE(9,FOR,38,ICAR,1,DATA(1),DATA(2),DATA(3),DATA(4),
     1 DATA(5),DATA(6),DATA(7),DATA(8),DATA(9),P,Q,R,S,T,NN,NM)
C
C     TOTAL EXPOSED
C
      DATA(1)=A(3)
      DATA(1)=DATA(1)*XL*XL/(SC*SC)
      DATA(2)=A(27)
      DATA(3)=A(7)
      DATA(4)=A(40)
      DATA(5)=A(16)
      DATA(6)=A(30)+XW+(A(21)-A(23))*TAN(A(35))-0.25*DATA(5)+XBN
      DATA(7)=A(31)+AIN(4)-AIN(3)
      DATA(5)=DATA(5)*XL/SC
      DATA(6)=DATA(6)*XL/SC
      DATA(7)=DATA(7)*XL/SC
      DATA(8)=CDO
      DATA(9)=CDF
      IF(CDO .EQ. UNUSED) DATA(8)=2.*UNUSED
      IF(CDF .EQ. UNUSED) DATA(9)=2.*UNUSED
      WRITE(6,1050)
      CALL SWRITE(9,FOR,38,ICAR,1,DATA(1),DATA(2),DATA(3),DATA(4),
     1 DATA(5),DATA(6),DATA(7),DATA(8),DATA(9),P,Q,R,S,T,NN,NM)
 1010 FORMAT(20H THEORITICAL INBOARD)
 1020 FORMAT(20H     EXPOSED INBOARD)
 1030 FORMAT(20H            OUTBOARD)
 1040 FORMAT(20H   TOTAL THEORITICAL)
 1050 FORMAT(20H       TOTAL EXPOSED)
      RETURN
      END

      SUBROUTINE CALCA(DYN,A,WINGIN)
C
C***  COMPUTES WING ACCELERATION PARAMETERS
C
      COMMON /OVERLY/ NLOG,NMACH,IM,NALPHA
      COMMON /CONSNT/ PI,DR,UNUSED,RAD
      COMMON /FLGTCD/ FLC(93)
      REAL MACOE
      REAL KGIN
      REAL   K6,LAME,KCMQ,KCLQ,KPG,MACH,LAMTR
      DIMENSION DYN(213),A(195),WINGIN(77)
      DIMENSION ROUTID(2)
      DIMENSION IEGIN(4),IGM(4),IKGIN(4),IEDM(4)
      DIMENSION XEGIN(14),YEGIN(14)
      DIMENSION XKGIN(17),YKGIN(17)
      DIMENSION XGM(12),YGM(12)
      DIMENSION XEDM(14),YEDM(14)
      DIMENSION QEGIN(2),QGM(1),QKGIN(2),QEDM(1)
      DATA ROUTID /4HCALC,4HA   /
      DATA IEGIN  /14,3*0/,IGM/12,3*0/,IKGIN/17,3*0/,IEDM/14,3*0/
      DATA QEGIN  /4HQEGI,4HN   /
      DATA QGM    /4HQGM /
      DATA QKGIN  /4HQKGI,4HN   /
      DATA QEDM   /4HQEDM/
      DATA XEGIN  /0.,.025,.05,.1,.15,.2,.3,.4,.5,.6,.7,.8,.9,1.0 /
      DATA YEGIN  /1.,1.,.996,.984,.969,.951,.912,.868,.827,.782,.743,
     1             .706,.669,.637/
      DATA XGM    /0.,.05,.1,.2,.3,.4,.5,.6,.7,.8,.9,1./
      DATA YGM/1.,.984,.96,.883,.802,.728,.657,.595,.540,.495,.459,.425/
      DATA XKGIN /0.,.025,.05,.1,.15,.2,.25,.3,.35,.4,.45,.5,.6,.7,.8,
     1            .9,1./
      DATA YKGIN /.14,.198,.229,.272,.305,.334,.358,.381,.401,.424,.444,
     1            .464,.501,.537,.571,.605,.637/
      DATA XEDM  /0.,.025,.05,.1,.15,.2,.3,.4,.5,.6,.7,.8,.9,1./
      DATA YEDM  /1.,1.,.997,.985,.969,.952,.913,.87,.827,.782,.743,
     1            .707,.669,.637/
C
      MACH=FLC(IM+2)
      BETA=SQRT(MACH**2-1.)
      B2=BETA
      PIO2=PI/2.
      D=(WINGIN(4)-WINGIN(3))*2.
      B=2.*WINGIN(4)
      DB=D/B
      K6=BETA/TAN(A(58)/RAD)
      CR=A(9)
      S=(CR+WINGIN(1))*WINGIN(4)
      SE=A(3)
      ARE=A(7)
      LAME=A(27)
      GG=BETA/(TAN(A(58)/RAD)+(4./ARE)*((LAME-1.)/(LAME+1.)))
      CALL INTERX(1,XEGIN,GG,IEGIN,YEGIN,EGIN,14,14,1,0,0,0,1,0,0,0,
     1            QEGIN,2,ROUTID)
      CALL INTERX(1,XGM,K6,IGM,YGM,GM,12,12,1,0,0,0,1,0,0,0,
     1            QGM,2,ROUTID)
      CALL INTERX(1,XKGIN,GG,IKGIN,YKGIN,KGIN,17,17,1,0,0,0,1,0,0,0,
     1            QKGIN,2,ROUTID)
      CALL INTERX(1,XEDM,K6,IEDM,YEDM,EDM,14,14,1,0,0,0,1,0,0,0,
     1            QEDM,2,ROUTID)
      EPM=1./EDM
      EPG=1./EGIN
      KPG=1./KGIN
      W=(4.*K6)/(ARE*B2*(1.+LAME))
      SN=1.-(1.-LAME)*W
      A1=(1.+K6)
      A2=(1.+SN)
      A3=(A1*A2+W*(K6-1.))*(W+SN-1.)
      A4=1.-K6
      A5=A1*SN+K6*W
      A6=A2+W
      A7=A2-W
      A8=A1-W
      A9=SN+K6
      IF(A2.LT.0.0)RETURN
      IF(A3.LT.0.0)RETURN
      IF(SN.GE.1.0.OR.(SN.LT.0.0))RETURN
      A4SQ=A4**2
      A1SR=SQRT(A1)
      A2SR=SQRT(A2)
      A3SR=SQRT(A3)
      A4SR=SQRT(A4)
      A31P5=A3*A3SR
      A11P5=A1*A1SR
      A3A1SR=A3SR*A1SR
      A42P5=A4SQ*A4SR
      A21P5=A2*A2SR
      A41P5=A4*A4SR
      A43PR=A4SQ*A4*A4SR
      SN2=SN**2
      SN3=SN*SN2
      SN2M1=SN2-1.
      SN2M1S=SQRT(-SN2M1)
      CM1=3.*(1.-PIO2/KPG)
      CM2=W*CM1
      CM3=(EPG-GG**2*KPG)/(KPG-EPG)
      SN4=SN2**2
      SN5=SN4*SN
      CM4=4.*SN5-12.*SN3+23.*SN
      A10=ARCSIN((A1*(SN2M1   )+W*(1.+SN*K6))/(W*A9))-ARCSIN(SN)
      A11=ARCSIN((W-1.-K6*(W+SN))/A9)+PIO2
      A12=(SN+W)*(SN-K6)-A9+2.*(W-1.)
      A13=ARCSIN(A12/(A6*A9))+PIO2
      OO=0.
      OOO=0.
      O4=0.
      IF(GG.GE.1..OR.GG.LE.0.)GO TO 1000
      OO=  ((8.0*K6*W**2)/GG)*(CM1 +(SN/W)*(2.-(EPG/KPG)
     1   -CM3)+(SN2/(2.0*W**2))*(1.-((PI*(1.-GG**2))/(4.0*(KPG-EPG)))))
      OOO=(2.*K6/(3.*GG))*(CM2  +SN*(1.-CM3))
      O4=(K6/GG)*(SN*(1.-EPG/KPG)+CM2)
 1000 CONTINUE
      Q=3.*GM*((W**4/(SN2M1   )**3)*((A8**2*(2.*SN**5+12.*SN**3+SN)+A5*
     1  (A8*(-27.*SN**2-3.)+A5*CM4   ))/(W**3*A9**3)*
     2  (A3A1SR     )+A10*
     3  (3.0*(2.*SN**2+3.)/(SN2M1S        ))-CM4       )+
     4  (A1SR  /A4**3)*((3.0*(2.*K6**2+3.)/A4SR  )*A11
     5  +((A8**2*(-2.*K6**5-12.*K6**3-K6)+A5*(A8*(-27.
     6  *K6**2-3.)+A5*(-4.*K6**5+12.*K6**3-23.*K6)))/(A9**3*A1**2))*A3SR
     7   )-OO)
C
      C=((8.0*A4*A9*(3.+27.*K6+37.*K6**2+13.*K6**3)+(12.+14.*K6+21.*K6**
     12+13.*K6**3)*(-3.*A4*A8+5.0*K6*A9))/(3.*A4**2*A9**4))*A3**1.5+((15
     2.*(8.+6.*K6+3.*K6**2+3.0*K6**3))/(2.*A4**3))*((((W-1.)-K6*(W+SN))/
     3(-A9**2))*A3**.5-(1.0/A4**.5)*A11)
     4-((((4.-2.*SN+9.*SN**2)*(3.+2.*K6)+5.*K6**2*(SN-2.))*(-6.*A2*A8-5.
     5*A9*A7)-2.0*A2*A9*(8.*W*(3.+2.*K6)*(9.*SN-1.)+20.*K6*(K6*W+(2.-SN)
     6*A1)))/(6.0*A2**2*A9**4))*A3**1.5
C
      F=(((4.0-2.*SN+9.*SN**2)*(3.+2.*K6)+5.*K6**2*(SN-2.))*(5.*A7**2+4.
     1*W*A2)+2.*A2*A7*(8.0*W*(3.+2.*K6)*(9.*SN-1.)+20.*K6*(K6*W+(2.-SN)*
     2A1))+16.0*W*A2**2*(9.*W*(3.+2.*K6)-5.*K6*A1))*((A12/
     3(-A9**2))*A3**.5-((A6**2)/(2.0*A2**.5))*A13)
C
      KCMQ=(-2.*K6**2)/(ARE*B2**2*(3.*W**2-3.*W*(1.0-SN)+(1.0-SN)**2)**2
     1)*(Q+(8.0/(5.*PI*A1**1.5))*(C-(1./(16.*A2**3))*F))
C
      O=((W*((2.*SN**2-1.0)*(SN**2-1.)*A1-W*(2.*SN**2*(1.-SN*K6)+(5.*SN*
     1K6+1.))))/(3.0*(SN**2-1.)**2*A9**2))*(A3*A1)**.5-((W**3*(2.*SN**3-
     25.0*SN))/(3.*(SN**2-1.)**2))-(W**3/((SN**2-1.)**2*(1.-SN**2)**.5))
     3*A10+(A1**.5/A4**2.5)*A11-(((-2.*K6**3*SN+5.*SN*K6+2.*K6**2+1.)-W*
     4(2.*K6**2-1.)*(K6-1.))/(3.*(K6-1.)**2*A9**2))*(A3*A1)**.5-OOO
C
      P=(((A9*(3.0-K6)*A1))/(3.*A2*A4*A9**3))*A3**1.5+((3.+K6)/A4**2)*((
     1((W-1.0)-K6*(W+SN))/(-2.*A9**2))*A3**.5-(1./(2.*A4**.5))*A11)-((A7
     2*(3.0*SN+2.*SN*K6+K6**2)+2.*A2*(W*(3.+2.*K6)-K6*A1))/A2**2)*((A12/
     3(-8.*A9**2))*A3**.5-(A6**2/(16.*A2**.5))*A13)
C
      KCLQ=((4.*K6)/(B2*(3.*W**2-3.*W*(1.-SN)+(1.-SN)**2)))*(3.*GM*O+(8.
     1/(PI*A1**1.5))*P)
C
      CMA=((2.*K6)/(B2*(3.*W**2-3.*W*(1.-SN)+(1.-SN)**2)))*((1./EPM)*(((
     1W*SN*(K6-1.0)**2*(3.*W*SN-W*K6*(SN**2-4.)-SN*(SN**2-1.)*A1)+K6*(SN
     2**2-1.0)**2*(4.*SN+3.*K6-SN*K6**2+K6*W*A4))/((SN**2-1.)**2*(K6-1.)
     3**2*A9**2))*(A3*A1)**.5+((W**3*(SN**2+2.))/((SN**2-1.)**2*(1.-SN**
     42)**.5))*A10+((W**3*(SN**3-4.*SN))/((SN**2-1.)**2))-((A1**.5*(K6**
     52+2.0))/A4**2.5)*A11+O4
     6  )+(8.0/(PI*A1**.5))*(((-(A3**1.5))/(A9**2*A4*A2))-((A7*(2.-SN)-2
     7.0*W*A2)/A2)*((A12/(-8.*A2*A9**2))*A3**.5-((A6**2)/(16.*A2**1.5))*
     8A13)-((2.*K6+1.)/A4)*((((W-1.)-K6*(W+SN))/(-2.*A4*A9**2))*A3**.5-(
     91./(2.*A4**1.5))*A11)))
C
      CLDA2=((4.*K6)/(B2**3*(3.*W**2-3.*W*(1.-SN)+(1.-SN)**2)))*(EDM*(((
     12.0*W*(K6**2-1.)*(SN**2-1.)
     2                +(1.+SN*K6)*(W**2*(K6-1.)+A1*(SN**2-1.)))/((SN**2-
     31.)*(K6-1.)*A9**2))*(A3*A1)**.5+(W**3/((SN**2-1.)*(1.-SN**2)**.5))
     4*A10-((SN*W**3)/(SN**2-1.))+(A1/A4)**1.5*A11)+(8./(PI*A1**.5))*(((
     5(K6-1.)*A6*(2.*A2*A8-A7*A9)+4.*A2*((1.+SN*K6)+W*(K6-1.)))/(4.*A2*A
     64*A9**2))*A3**.5+(A6**3/(8.*A2**1.5))*A13-(1./A4**1.5)*A11))
C
      CMDA2A=
     A      ((6.*K6**2)/(ARE*B2**4*(3.0*W**2-3.*W*(1.-SN)+(1.-SN)**2)**2
     1))
C
      CMDA2B=
     A   (3.0*EDM*((1./(3.*(SN**2-1.)**3*(K6-1.)**3*A1**1.5*A9**3))*(A8*
     2*2*(W*(K6-1.)**3*A1**2*(-2.*SN**5+SN**3+SN)-(SN**2-1.)**3*(2.*K6**
     35-K6**3-K6))+A5*A8*(W*(K6-1.)**3*A1**2*(3.*SN**4-3.)-(SN**2-1.)**3
     4*(3.0*K6**4-3.))+A5**2*(W*(K6-1.)**3*A1**2*(2.*SN**5-7.*SN**3+5.0*
     5SN)+(SN**2-1.0)**3*(2.*K6**5-7.*K6**3+5.*K6)))*A3**.5+(A1**1.5/A4*
     6*2.5)*A11-((W**4*(2.*SN**5-7.*SN**3+5.*SN))/(3.*(SN**2-1.)**3))
     6                                                      -(W**4/(1.-S
     7N**2)**2.5)*A10))
C
      CMDA2C=         ((8./(5.*PI*A1**.5))*((((5.0*SN*((SN+W)*A4+A1)+10.
     8*K6)*A3**1.5)/(A2*A4*A9**3))+((5.0*(2.+K6))/A4**2)*(((W*A4-(1.+SN*
     9K6))/(-A9**2))*A3**.5-(1./A4**.5)*A11)-((5.0*(2.*(3.*W+2.)*(W+SN**
     A2)-(2.+SN-10.*SN*W-3.*SN**3-3.*SN*W**2)))/(8.*A2**2))*(((-2.*A2*A8
     B+A9*A7)/(-A9**2))*A3**.5-(A6**2/(2.0*A2**.5))*A13)))
C
      CMDA2=CMDA2A*(CMDA2B+CMDA2C)
      CMDA2=-CMDA2
      CMDA3A=
     A      (2.*MACH**2/(B2**2))
      CMDA3B=
     A                           ((3.0*K6**2)/(ARE*B2**2*(3.0*W**2-3.*W*
     1(1.-SN)+(1.-SN)**2)**2))*((3.0/EPM)*((1./(3.*A9**3*(SN**2-1.)**3*(
     2K6-1.)**3*A1**1.5))*(A8**2*(W*(K6-1.)**3*A1**2*(11.*SN**3+4.*SN**5
     3)+(SN**2-1.)**3*(11.0*K6**3+4.*K6**5))-A5*A8*(W*(K6-1.)**3*A1**2*(
     427.*SN**2+3.*SN**4)-(SN**2-1.0)**3*(27.*K6**2+3.*K6**4))+A5**2*(W*
     5(K6-1.)**3*A1**2*(2.0*SN**5-5.*SN**3+18.*SN)-(SN**2-1.)**3*(5.*K6*
     6*3-18.0*K6-2.*K6**5)))*A3**.5-((W**4*(2.*SN**5-5.*SN**3+18.*SN))/(
     73.0*(SN**2-1.)**3))+((W**4*(3.*SN**2+2.))/((SN**2-1.)**3*(1.-SN**2
     8)**.5))*A10+(((3.0*K6**2+2.)*A1**.5)/A4**3.5)*A11))
C
      CMDA3C=
     A                                                  ((4./(5.*PI*A1**
     9.5))*(((A4**2*A9*(5.*A7*(8.0-4.*SN+3.*SN**2)-16.*W*A2*(2.-3.*SN))+
     A6.0*A2*A4*A8*(A4*(8.-4.*SN+3.*SN**2)-A2*(8.+4.*K6+3.*K6**2))+2.*A2
     B**2*A9*(16.0+64.*K6+4.*K6**2-9.*K6**3))/(3.*A2**2*A4**2*A9**4))*A3
     C**1.5-((5.*(4.*W*A2*(3.*SN**2+4.*W)+A7**2*(8.-4.*SN+3.*SN**2)))/(8
     D.0*A2**3))*(((-2.*A2*A8+A7*A9)/(-A9**2))*A3**.5-(A6**2/(2.*A2**.5)
     E)*A13)+((5.0*(4.+4.*K6+7.*K6**2))/A4**3)*(((W*A4-(1.+SN*K6))/(-A9*
     F*2))*A3**.5-(1./A4**.5)*A11)))
C
      CMDA3=CMDA3A*(CMDA3B+CMDA3C)
      DYN(16)=CLDA2*B2**2
      DYN(27)=CMDA2*B2**2
      DYN(22)=KCLQ+2.*CMA
      DYN(26)= KCMQ+CMDA3*B2**2/MACH**2
      RETURN
      END
